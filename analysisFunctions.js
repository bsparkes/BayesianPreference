var getMaxLengthOfPref = function (data) {
    var maxLength = 0;
    for (let i = 0; i < data.length; i++) {
        if (data[i][0].length > maxLength) {
            maxLength = data[i][0].length;
        }
    }
    return maxLength;
};

var arrifyJSONPref = function(prefOrder) {
    result = (prefOrder.slice(2,prefOrder.length-2)).split('],[');

    for (let i = 0; i < result.length; i++) {
        result[i] = result[i].split(',');
    }

    for (let i = 0; i < result.length; i++) {
        for (let j = 0; j < result[i].length; j++) {
            result[i][j] = ~~result[i][j];
        }
    }
    return result;
};

var listOfAnalysisPrefs = function(analysis) {
    var list = [];
    for (var key in analysis) {
        if (analysis.hasOwnProperty(key)) {
            list.push(key);
        }
    }
    return list;
};

var conditionaliseAnalysis = function(analysis) {
    let conditionalAnalysis = analysis;
    let analysisPrefs = listOfAnalysisPrefs(analysis);

    for (let i = 0; i < analysisPrefs.length; i++) {

        let key = analysisPrefs[i];
        let keyArray = arrifyJSONPref(key);

        if (keyArray.length > 1) {

            let priorArray = getSubPref(keyArray, keyArray.length - 2);
            let priorArrayJSON = JSON.stringify(priorArray);
            let priorKey = priorArrayJSON;
            conditionalAnalysis[key] =
                (conditionalAnalysis[key]/conditionalAnalysis[priorKey]);

        }
    }
    return conditionalAnalysis;
};


/*
 * The functions below are for when the preference orderings require some
 * interpretation, typically due to the presence of \top or \bot in the order at
 * some problematic point. So far they only deal with occurrences of \top. \bot
 * requires more complex adjustments.
 */

/*
 * The following uniqBy is taken from
 * http,//stackoverflow.com/questions/9229645/
 * remove-duplicate-values-from-js-array.
 *  It does the job fairly well.
 */

/*
 * The only thing to adjust is getting it to vary the inconsistent states which
 *  start coming about.
 */

var prefNormalForm = function(array) {
    var seen = {'[0,0,0,0]': true};
    return array.filter(function(item) {
        var k = JSON.stringify(item);
        return seen.hasOwnProperty(k) ? false : (seen[k] = true);
    });
};

/*
 * Now we want to generate a right branching tree of all considered preference
 * orderings, where each branch takes a different element of the order.
 * All of the following works strictly on arrays of preference orders, so some
 * adjusting of the output from the infer function is required.
 */

/*
 * The first step is to get a normal form version of the output.
 * Basically combining duplicate orders and and their respective probabilities.
 */

/*
 * Input is always an array of arrays, in which the first element is the
 * preference order and the second element is the probability.
 */

/*
 * First a function to get the formal form of an array.
 */

var normalFormData = function(array) {
    var NFDict = {};
    return array.filter(function(item) {
        var k = JSON.stringify(prefNormalForm(item[0]));
        item[1] = 0;
        return NFDict.hasOwnProperty(k) ? false : (NFDict[k] = true);
    });
};

/*
 * We put the above together, with a deep clone of the array.
 */

var probabilitiesForPossiblePaths = function(array) {
    var tempArray = JSON.parse(JSON.stringify(array));
    // We need a deep clone of the array!
    var pathWithProb = normalFormData(tempArray);
    for (var i = 0; i < array.length; i++) {
        for (var j = 0; j < pathWithProb.length; j++) {
            if (JSON.stringify(prefNormalForm(array[i][0])) ==
                JSON.stringify(pathWithProb[j][0])) {
                pathWithProb[j][1] += array[i][1];
            };
        };
    };
    return pathWithProb;
};

/*
 * Now, the task is to strip any order which no longer has any positive
 * probability.
 */

/* The thing now is probably to break down the data array to find all the
 * sub-preference orders. With this in hand, run through the array to get a
 * lexicgraphic order, and then  go through this ordered one and determine
 * the probabilities to assign.
 */

var stripProbabilities = function(array) {
    output = JSON.parse(JSON.stringify(array));
    for (let i = 0; i < output.length; i++) {
        output[i] = output[i][0];
    }
    return output;
};

