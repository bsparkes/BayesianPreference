var analyse = function(data, maxPrefLength = getMaxLengthOfPref(data)) {
    // data is always a two element array of pref order and prob.
    var analysis = {};
    var numVals = data[0][0][0];
    // var maxPrefLength = getMaxLengthOfPref(data)

    for (let i = 0; i < maxPrefLength; i++) {
        for (let j = 0; j < data.length; j++) {
            if (i < data[j][0].length) {
                var subPref = getSubPref(data[j][0], i);
                var subPrefString = JSON.stringify(subPref);
                if (analysis.hasOwnProperty(subPrefString)) {
                    analysis[subPrefString] += data[j][1];
                } else {
                    analysis[subPrefString] = data[j][1];
                }
            }
        }
    }
    return analysis;
};

var probAtIndex = function (data, index) {
    modifiedData = JSON.parse(JSON.stringify(data));
    for (let i = 0; i < data.length; i++) {
        modifiedData[i][0] = modifiedData[i][0].slice(index);
    }
    return analyse(modifiedData);
};

var getSubPref = function(prefOrder, length) {
    //preforder is an array
    var subPref = [];
    for (let i = 0; i < length + 1; i++) {
        subPref = subPref.concat([prefOrder[i]]);
    }
    return subPref;
};

